                <h1> HIGIENIZAÇÃO DE AR CONDICIONADO </h1></br>
                <figure>
                    <img src="image/arcondicionado.jpg" alt="arcondicionado">
                </figure></br>
                <p>O sistema de ar condicionado está constantemente úmido, sendo um ambiente ideal para a proliferação de fungos e bactérias.
                    Carros que não possuem o filtro anti-pólen acabam acumulando em seu sistema resíduos sólidos, como folhas, poeira e até mesmo                     pequenos insetos. Por isto é extremamente importante fazer a higienização deste sistema e quando possível instalar e/ou substituir o             filtro anti-pólen.
                    A freqüência de higienização e troca do filtro vai de acordo com o ambiente em que o veiculo roda (poluição, transito, terra…) e a               freqüência com que funciona o sistema de ar condicionado. Porém estes prazos não podem ultrapassar em 15.000km para o filtro e                   30.000km para a higienização.</p>
